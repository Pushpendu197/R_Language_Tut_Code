# numeric
x <- 10.5
print(class(x))
print(typeof(x))

# integer
x <- 1000L
print(class(x))
print(typeof(x))

# complex
x <- 9i + 3
print(class(x))
print(typeof(x))

# character/string
x <- "R is exciting"
print(class(x))
print(typeof(x))

# logical/boolean
x <- TRUE
print(class(x))
print(typeof(x))